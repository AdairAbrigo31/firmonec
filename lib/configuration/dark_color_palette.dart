
import 'package:flutter/material.dart';

class DarkColorPalette {
  static Color primaryColor() {
    return const Color(0xFF0A0E24); // Azul oscuro para el header
  }

  static Color secondaryColor() {
    return const Color(0xFFFF4757); // Rojo más brillante para Cancelar
  }

  static Color accentColor() {
    return const Color(0xFF1E90FF); // Azul más brillante para Firmar
  }

  static Color backgroundColor() {
    return const Color(0xFF121212); // Gris muy oscuro para el fondo
  }

  static Color surfaceColor() {
    return const Color(0xFF1E1E1E); // Gris oscuro para las tarjetas
  }
}